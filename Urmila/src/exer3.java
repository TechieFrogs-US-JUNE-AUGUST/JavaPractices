public class exer3 {
    public static void main (String[] args){//swapping numbers
        int a = 4, b = 7;
        System.out.println("Print numbers before swapping" );
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        int num = a;
        a = b;
        b = num;
        System.out.println("Print numbers after swappng");
        System.out.println("a =" + a);
        System.out.println("b =" + b);
    }

}