public class NumberisEvenorOdd 
{

    public static void main(String[] args)
    {
     int num = 20;
     if( num % 2 == 0) // if the number is divisible by 2 then it's an even number
      {                // else odd number
         System.out.println("Entered number is even");
      }                //print if it is even number
     else
      {
         System.out.println("Entered number is odd");
      }               // print if it is odd number
    }
}