package Arrays;

public class Arrays5PrintTheElements 
{
    public static void main(String[] args)
    {
     int[]arr = new int[]{1,2,3};//initializing array
     System.out.println("Elements in given array: ");
     for(int i=0;i<arr.length;i++)// loop used for arrray by incrementing i value
     {
         System.out.println(arr[i]+"");
     }
    }       
    }
