package strings;

public class String43 {
    public static void main(String[] args) {
        String str="String Assignment";
        for(int i=0;i<str.length();i++)
        {
            char a= str.charAt(i); //display individual character of the string
            System.out.print(a + " ,");
        }
        System.out.println();

    }
    
}
