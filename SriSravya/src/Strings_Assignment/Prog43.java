//TO SEPERATE THE INDIVIDUAL CHAR FROM A STRING
package Strings_Assignment;

public class Prog43 {
    public static void main(String[] args) {
        String s= "Sri Sravya";
        for(int i=0; i<s.length(); i++){
            System.out.print(s.charAt(i)+" ");        //displays the each iterated character
        }
    }
}
