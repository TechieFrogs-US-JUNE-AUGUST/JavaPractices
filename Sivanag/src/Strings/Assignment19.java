//Check if string contains a substring using contains()
package Strings;
public class Assignment19 {
    public static void main(String[] args) {
        String st ="How are you";
        System.out.println(st);
        System.out.println("are :" + st.contains("are"));
        System.out.println("hi :" + st.contains("hi"));
        System.out.println("how :" + st.contains("how"));
        System.out.println("How :" + st.contains("How"));
        System.out.println("you :" + st.contains("you"));
    }  
}
